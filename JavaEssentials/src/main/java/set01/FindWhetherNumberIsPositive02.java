package set01;

public class FindWhetherNumberIsPositive02 {

	public static void main(String[] args) {
		int num = -3;
		if(num > 0)
		{
			System.out.println("The number is positive");
		}
		else if(num == 0)
		{
			System.out.println("The number is zero");
		}
		else
		{
			System.out.println("The number is negative");
		}
	}

}
